shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300], ['педаль', 100],
         ['седло', 1500], ['рама', 12000], ['обод', 2000], ['шатун', 200], ['седло', 2700]]

detail = input('Название детали: ')
det_c = 0
det_sum = 0

for i in shop:
    if i[0] == detail:
        det_c += 1
        det_sum += i[1]


print(f'Количество деталей: {det_c}')
print(f'Общая стоимость: {det_sum}')